package MODEL;

import utils.tools;


public class operator{
	int ID,status;
	String account,pass,name,tel,lastdate,powers;
	String comments;
	
	
	
	public operator( String powers,int iD) {
		super();
		ID = iD;
		this.powers = powers;
	}
	public operator(String account, String pass) {
		super();
		this.account = account;
		this.pass = pass;
	}
	public String getStatusname() {
		return tools.status[status];
	}
	public operator() {
		super();
	}
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	public String getAccount() {
		return account;
	}
	public void setAccount(String account) {
		this.account = account;
	}
	public String getPass() {
		return pass;
	}
	public void setPass(String pass) {
		this.pass = pass;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	public String getLastdate() {
		return lastdate;
	}
	public void setLastdate(String lastdate) {
		this.lastdate = lastdate;
	}
	public String getPowers() {
		return powers;
	}
	public void setPowers(String powers) {
		this.powers = powers;
	}
	public String getComments() {
		return comments;
	}
	public void setComments(String comments) {
		this.comments = comments;
	}
	public operator(int iD, int status, String account, String pass,
			String name, String tel, String lastdate, String powers,
			String comments) {
		super();
		ID = iD;
		this.status = status;
		this.account = account;
		this.pass = pass;
		this.name = name;
		this.tel = tel;
		this.lastdate = lastdate;
		this.powers = powers;
		this.comments = comments;
	}
	public operator(int iD, String pass) {
		super();
		ID = iD;
		this.pass = pass;
	}
	
}
